package com.control_statement.java;

public class Do_While_Statement {

	public static void main(String[] args) {
		
		int x=1;//initialization
		do
		{
			System.out.println(x+" ");//statement
			x++;//increment/decrement
		}while(x<=100);//condition
		
		/*
		 * even and odd num in reverse order.
		 */
	}

}
