package com.control_statement.java;

public class While_Loop {
	
	public static void main(String[] args) {
		/*
		 * initialization
		 * while(Condition)
		 * {
		 *    statement;
		 *    increment/decrement;
		 * }
		 * 
		 */
		int i = 1;
		while(i<=100)
		{
			System.out.println(i);
			i++;
		}
		/*
		 * even and odd num in reverse order.
		 */
	}

}
