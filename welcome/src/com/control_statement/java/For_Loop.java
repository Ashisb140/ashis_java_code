package com.control_statement.java;

public class For_Loop {

	public static void main(String[] args) {
		
		/*
		 * What's loop?
		 * if u want to execute single line of statement more than once then we use loops
		 * Ex: 1-10 print
		 *
		 * 
		 * for(initialization;condition;increment/decrement)
		 * {
		 *    statement;
		 * }
		 * 
		 */
		
		for(int i=1;i<=100;++i)
		{
			if(i%2!=0)
				System.out.print(i+" ");
		}
		
		/*
		 * even and odd num in reverse order.
		 */

	}

}
